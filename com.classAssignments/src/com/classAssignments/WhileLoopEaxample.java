package com.classAssignments;

public class WhileLoopEaxample {

	public static void main(String[] args) {
		int i=10;
		int j=10;
		while(i>=1) {
			System.out.print(i+" ");
			i--;
		}
		System.out.println();
		System.out.println("-------------");
		do {
			System.out.print(j+" ");
			j--;
		}while(j>=1);

	}

}
